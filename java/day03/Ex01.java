class Ex01 {
  public static void main(String[] args) {
    System.out.println(5+2);
    System.out.println(5-2);
    System.out.println(5*2);
    System.out.println(5%2);

    System.out.println(5/2); // 리터럴 타입 int
    System.out.println(5/2.0); // 리터럴 자동형변환
    System.out.println((double)5/2); // 리터럴 자동형변환
  }
}